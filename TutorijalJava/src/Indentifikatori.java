
public class Indentifikatori {

	public static void main(String[] args) {
		/**
		 Identifikatori (identifiers) služe za označavanje svih vrsta elemenata programa: podataka, simboličkih konstanti, 
		 tipova podataka koje definiše programer, potprograma i oznaka koje služe kao odredišta za naredbe skokova.
		  
		  Pravila kojih se moramo pridržavati pri korišćenju identifikatora su sledeće:
		  
		  -	Svi identifikatori počinju slovom, donjom crtom (_) ili znakom dolar ($).
		  -	Prvi znak ne sme da bude cifra. Posle prvog znaka identifikator može da sadrži bilo koju kombinaciju znakova. 
		  - Ključne reči se ne smeju koristiti za identifikatore. 
		  
		 Pravi se razlika izmedju malih i velikih slova (case sensitivity), tako da su A i a dva različita identifikatora.
		  
		  Primeri ispravnog identifikatora - a, a123, $plata, _vrednost, _vrednost2 ...
		  Primeri neispravnog identifikatora - 123a, -ime, void, 1$plata ...
		 */
	}

}
